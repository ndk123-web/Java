import java.util.*;

public class LinkHashSet {
    public static void main(String[] args) {

        Set<Integer> lh = new LinkedHashSet<>();

        // Data stores in linear way
        lh.add(10);
        lh.add(20);
        lh.add(30);
        lh.add(40);

        System.out.println(lh);

        // All Methods will be same as HashSet

    }
}
